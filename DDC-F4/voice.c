#include "main.h"
#include "port.h"

#include "voice.h"
#include "Delay.h"
   
/*--------------------------------------------------
	SC_Speech()
	
	调用语音芯片，播报语音提示，几次脉冲表示播放第
	几段语音提示
---------------------------------------------------*/
void SC_Speech(tByte cnt)
	{                   
	tByte i;
	SC_RST = 0;
	delay_ms(100);
	SC_RST = 1;
	delay_ms(150);
	for(i=0;i < cnt;i++)
		{
		SC_DATA = 1; 	// 数据脉冲高
		delay_us(20); 	// 延时 100US
		SC_DATA = 0; 	// 数据脉冲低
		delay_us(20); 	// 延时 100US
		}
	}

/*-------------------------------------------------
	noVoice()
	
	在上电的时候调用，屏蔽语音的误报
--------------------------------------------------*/
void noVoice()
	{
	P14 = 0;
	SC_DATA = 0;
	SC_RST = 0;
	delay_us(100);	//上电复位防止干扰发声
	SC_RST = 1;
	delay_us(100);
	P14 = 1;
	}

/*-------------------------------------------------------------
	Alarm_stolen_speech()
	被盗之后的报警语音，附机接收到主机的被盗信号，发出语音提示
---------------------------------------------------------------*/
void Alarm_stolen_speech(void)
	{
	Voice_EN = 1;
	SC_Speech(4);
	Delay(150);
	Voice_EN = 0;
	}
	
/*--------------------------------------------------------------
	Alarm_raised_speech()
	主机被抬起时，给附机发信号，附机语音提示主机被抬起。
---------------------------------------------------------------*/
void Alarm_raised_speech(void)
	{
	Voice_EN = 1;
	SC_Speech(3);
	Delay(140);
	Voice_EN = 0;
	}
	
/*--------------------------------------------------------------
	Alarm_fell_speech()
	主机摔倒在地上后，附机语音提示主机倒地上了。
----------------------------------------------------------------*/
void Alarm_fell_speech(void)
	{
	Voice_EN = 1;
	SC_Speech(2);
	Delay(120);
	Voice_EN = 0;
	}
	
/*---------------------------------------------------------------
	Battery_low_alarm_speech()
	电量低于一定值的时候，附机语音提示电量不足
----------------------------------------------------------------*/
void Battery_low_alarm_speech(void)
	{
		Voice_EN = 1;
		SC_Speech(9);
		Delay(120);
		Voice_EN = 0;
	}

/*---------------------------------------------------------------
	Battery_high_alarm_speech()
	附机电池电量高于一定值，表示电池充满电，语音提示充满
----------------------------------------------------------------*/
void Battery_high_alarm_speech(void)
	{
	Voice_EN = 1;
	SC_Speech(14);
	Delay(120);
	Voice_EN = 0;
	}
	
/*---------------------------------------------------------------
	Host_battery_high_alarm_speech()
	当主机电充满后，发送信号给附机，附机收到后，语音提示主机充满电
	了。
---------------------------------------------------------------*/
void Host_battery_high_alarm_speech(void)
	{
		Voice_EN = 1;
		SC_Speech(7);  
		Delay(80);
		Voice_EN = 0;
	}

/*---------------------------------------------------------------
	battery_stolen_speech()
	接到主机电瓶被盗的信号后，语音提示
---------------------------------------------------------------*/
void battery_stolen_speech(void)
	{
	Voice_EN = 1;
	SC_Speech(11);  
	Delay(50);
	Voice_EN = 0;
	}

